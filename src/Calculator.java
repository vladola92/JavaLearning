public class Calculator {

    public  int adding(int x, int y) {
        int resultat = x + y;
        return resultat;
    }

    public  int subtracting(int x, int y) {
        int res = x * y;
        return res;
    }

    public  int divide(int x, int y) {
        int res = x / y;
        return res;
    }

    public  int average(int a, int b, int c) {
        int res = (a + b) / c;
        return res;
    }
}
